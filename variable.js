// Sequential flow
var a = 10
var b = 12
var sum = a + b;
var sub = a - b;
var div = a / b;
var multiply = a * b;

console.log(a, "\n", b);
console.log("Sum: " + sum);
console.log("Substracion: " + sub);
console.log("Divide: " + div);
console.log("Multiply: " + multiply);
console.log("\n");

